bool g_bWindow;
int g_nCntTime;

//=========================================================================================================//
// * Titleの初期化処理
//=========================================================================================================//
void InitTitle(void)
{
	SetBGWindow(D3DXVECTOR3(SCREEN_WIDTH * 0.5f, SCREEN_HEIGHT * 0.5f, 0.0f), COLLAR{ 255,255,255, 255 }, 0.0f, 1.0f, SCREEN_WIDTH / 2, SCREEN_HEIGHT / 2, 5, WINDOWSTATE_NORMAL);
	SetWindow(D3DXVECTOR3(SCREEN_WIDTH * 0.5f, 0, 0.0f), D3DXCOLOR{ 1.0f,1.0f,1.0f, 0.0f }, 150, 30, 1,0, 0, WINDOWSTATE_FADEIN, WINDOWUSE_NORMAL);
	SetWindow(D3DXVECTOR3(SCREEN_WIDTH * 0.5f, SCREEN_HEIGHT * 0.7f, 0.0f), D3DXCOLOR{ 1.0f,0.99f,0.05f, 0.0f }, 150, 30, 0, 0, 0, WINDOWSTATE_FLASH, WINDOWUSE_NORMAL);
	PlaySound(SOUND_LABEL_RANKING000);
}
//=========================================================================================================//
// * Titleの終了処理
//=========================================================================================================//
void UninitTitle(void)
{
	BreakWindow();
	BreakBGWindow();
	StopSound();
}
//=========================================================================================================//
// * Titleの更新処理
//=========================================================================================================//
void UpdateTitle(void)
{
	if (GetKeyboardTrigger(DIK_RETURN) == true)
	{
		SetFade(MODE_TUTORIAL, 0);
		PlaySound(SOUND_LABEL_BOTTON002);
	}
}
//=========================================================================================================//
// * Titleの描画処理
//=========================================================================================================//
void DrawTitle(void)
{
	DrawFont();
}