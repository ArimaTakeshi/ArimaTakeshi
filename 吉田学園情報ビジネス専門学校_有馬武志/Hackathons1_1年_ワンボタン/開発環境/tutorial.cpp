//=========================================================================================================//
// * ゲームの初期化処理
//=========================================================================================================//
void InitTutorial(void)
{
	SetWindow(D3DXVECTOR3(SCREEN_WIDTH * 0.5f, SCREEN_HEIGHT * 0.9f, 0.0f), D3DXCOLOR{ 1.0f,0.99f,0.05f, 0.0f }, 150, 30, 0, 0, 0, WINDOWSTATE_FLASH, WINDOWUSE_NORMAL);
	SetBGWindow(D3DXVECTOR3(SCREEN_WIDTH * 0.5f, SCREEN_HEIGHT * 0.5f, 0.0f), COLLAR{ 255,255,255, 255 }, 0.0f, 1.0f, SCREEN_WIDTH / 2, SCREEN_HEIGHT / 2, 6, WINDOWSTATE_NORMAL);
}
//=========================================================================================================//
// * ゲームの終了処理
//=========================================================================================================//
void UninitTutorial(void)
{
	BreakWindow();
	BreakBGWindow();
}
//=========================================================================================================//
// * ゲームの更新処理
//=========================================================================================================//
void UpdateTutorial(void)
{
	if (GetKeyboardTrigger(DIK_RETURN) == true)
	{
		SetFade(MODE_GAME, 0);
		PlaySound(SOUND_LABEL_BOTTON002);
	}
}
