//=========================================================================================================//
// * ゲームの初期化処理
//=========================================================================================================//
void InitResult(void)
{
	PLAYER *pPlayer = GetPlayer();
	InitDigital();
	PlaySound(SOUND_LABEL_RESULT000);

	SetDigital(D3DXVECTOR3(SCREEN_WIDTH * 0.6f, SCREEN_HEIGHT * 0.5f, 0.0f), D3DXCOLOR(0.41f, 1.0f, 0.3f, 0.0f), DIGITALSTATE_WAVE0, 30, 45, pPlayer[0].nScore);
	SetBGWindow(D3DXVECTOR3(SCREEN_WIDTH * 0.5f, SCREEN_HEIGHT * 0.5f, 0.0f), COLLAR{ 255,255,255, 255 }, 0.0f, 1.0f, SCREEN_WIDTH / 2, SCREEN_HEIGHT / 2, 6, WINDOWSTATE_NORMAL);
}
//=========================================================================================================//
// * ゲームの終了処理
//=========================================================================================================//
void UninitResult(void)
{
	BreakBGWindow();
	UninitDigital();
	StopSound();
}
//=========================================================================================================//
// * ゲームの更新処理
//=========================================================================================================//
void UpdateResult(void)
{
	//UpdateFont();
	UpdateDigital();

	if (GetKeyboardTrigger(DIK_RETURN) == true)
	{
		SetFade(MODE_TITLE, 0);
	}
}
//=========================================================================================================//
// * ゲームの描画処理
//=========================================================================================================//
void DrawResult(void)
{
	DrawDigital();
}
