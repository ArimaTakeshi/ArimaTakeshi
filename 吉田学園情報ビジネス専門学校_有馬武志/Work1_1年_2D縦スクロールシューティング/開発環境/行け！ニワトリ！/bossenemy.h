#ifndef _BOSSENEMY_H_
#define _BOSSENEMY_H_

//*************************************************************************************************************************
// インクルードファイル
//*************************************************************************************************************************
#include "main.h"

//*************************************************************************************************************************
// マクロ定義
//*************************************************************************************************************************
#define	MAX_ENEMY	(256)	//敵の最大数

//*************************************************************************************************************************
// 構造体定義
//*************************************************************************************************************************
typedef enum
{
	 BOSSENEMYSTATE_NORMAL = 0,
	 BOSSENEMYSTATE_DAMAGE,
	 BOSSENEMYSTATE_MAX
} BOSSENEMYSTATE;

typedef struct
{
	D3DXVECTOR3 pos;		//位置
	D3DXVECTOR3 move;		//移動
	int nType;				//種類
	bool bUse;				//使用しているかどうか
	 BOSSENEMYSTATE state;		//敵の状態
	int nLife;				//体力
	int nCounterState;		//状態管理のカウンター
}BossEnemy;

//*************************************************************************************************************************
// プロトタイプ宣言
//*************************************************************************************************************************
void InitBossEnemy(void);										//敵初期化処理
void UninitBossEnemy(void);										//敵終了処理
void UpdateBossEnemy(void);										//敵更新処理
void DrawBossEnemy(void);										//敵描画処理
void SetBossEnemy(D3DXVECTOR3 pos, int nType);					//敵の設定処理
BossEnemy *GetBossEnemy(void);								//敵の取得
void HitBossEnemy(int nCntEnemy, int nDamage);

#endif